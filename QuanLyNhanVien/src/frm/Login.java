    private boolean KiemTra(String tdn, String mk) {
        boolean kq = false;

        String cautruyvan = "select * from [User] where TaiKhoan = '" + tdn + "' and MatKhau = '" + mk + "'";
        System.out.println(cautruyvan);
        ResultSet rs = D17CNPM2.D17CNPM2.connection.executeQuery(cautruyvan);

        try {
            if (rs.next()) {
                kq = true;
                quyen = rs.getInt("MaQuyen");
                ten = rs.getString("HoTen");
                user = rs.getString("TaiKhoan");
                System.out.println("Ten nhan vien: " +ten+ "\nTai khoan: " +user+ "\nMa quyen: " +quyen);
            }
        } catch (SQLException ex) {
            System.out.println("Loi dang nhap");
        }

        return kq;
    }
