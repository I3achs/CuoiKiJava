    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String taiKhoan = txtTK.getText().trim();
        String matKhau = String.valueOf(txtMK.getPassword()).trim();
        
        String cautruyvan = "select * from [User] where TaiKhoan = '"+taiKhoan+"' and MatKhau = '"+matKhau+"'";
        ResultSet rs = D17CNPM2.D17CNPM2.connection.executeQuery(cautruyvan);
        String luumk = "", luutk ="";
        if(checkVisible.isSelected()){
            luutk = taiKhoan;
            luumk = matKhau;
            System.out.println("Luu mat khau.");
        } else {
            luutk = "";
            luumk = "";
            System.out.println("Khong luu mat khau.");
        }
        try {
            File file = new File("luumk.txt");
            if (!file.exists()) {
                file.createNewFile();
            }
            FileWriter fw = new FileWriter(file.getAbsoluteFile());
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(luutk+"\n"+luumk);
            bw.close();
            if(checkVisible.isSelected())
            System.out.println("Luu phien dang nhap thanh cong");
        } catch (IOException e) {
            e.printStackTrace();
        }
        
        if(KiemTra(taiKhoan, matKhau)){
            D17CNPM2.D17CNPM2.Home.setVisible(true);
            this.dispose();
        } else {
            ThongBao("Sai tai khoan hoac mat khau", "Loi dang nhap", 2);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

